public class Ond_forLoop 
{
    public static void main(String args[])
    {
        
    int a[] = {10,20,30,40,50};
    System.out.println("One Dimensionl Elements Are: \n");
    
    for(int i = 0; i < a.length; i ++)
    {
        System.out.println("a[" + i + "]:----->" + a[i]);
    }
  }
}
